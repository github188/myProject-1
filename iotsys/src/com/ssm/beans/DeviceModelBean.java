package com.ssm.beans;



@SuppressWarnings("serial")
public class DeviceModelBean  implements
		java.io.Serializable {
	private String id;
	private String typeName;
	private Integer identification;
	private String createTime;
	private String updateTime;
	private String comments;

	// Constructors

	/** default constructor */
	public DeviceModelBean() {
	}

	public DeviceModelBean(String id, String typeName, Integer identification,
			String createTime, String updateTime, String comments) {
		super();
		this.id = id;
		this.typeName = typeName;
		this.identification = identification;
		this.createTime = createTime;
		this.updateTime = updateTime;
		this.comments = comments;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public Integer getIdentification() {
		return identification;
	}

	public void setIdentification(Integer identification) {
		this.identification = identification;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

	@Override
	public String toString() {
		return "DeviceModel [id=" + id + ", typeName=" + typeName
				+ ", identification=" + identification + ", createTime="
				+ createTime + ", updateTime=" + updateTime + ", comments="
				+ comments + "]";
	}


}
